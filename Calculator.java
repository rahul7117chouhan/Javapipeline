public class Calculator {
    public static void main(String[] args) {
        int a = 20;
        int b = 9;

        int sum = a + b;
        int difference = a - b;

        System.out.println("Addition of " + a + " and " + b + " is: " + sum);
        System.out.println("Subtraction of " + b + " from " + a + " is: " + difference);
    }
}
